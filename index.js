function sumOddEven(N) {
  let juftSonYigindisi = 0;
  let toqSonYigindisi = 0;
  for(let i = 1;  i <= N; i++){
if ( i % 2 === 0){
  juftSonYigindisi += i;
}
else{
  toqSonYigindisi += i;
}
}
return [juftSonYigindisi, toqSonYigindisi];
}
